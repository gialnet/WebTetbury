                        <h2>MyEmpresa ofrece el nuevo sistema de pagos europeo que entrará en vigor el 1 de febrero de 2014.</h2>
                        <!--<div class=""><img src="images/euro.png" alt="SEPA" /></div>-->
                        <h5>¿Qué es el SEPA?</h5>

                        <p>La zona única de pagos en euros (SEPA por su acrónimo en inglés) es el área en el que ciudadanos, empresas y otros agentes económicos pueden efectuar y recibir pagos en euros en Europa, dentro y fuera de las fronteras nacionales, en las mismas condiciones y con los mismos derechos y obligaciones, independientemente del lugar en que se encuentren.</p>

                        </br>

                        <h5>¿Qué beneficios aporta el SEPA?</h5>

                        <p>SEPA permite que los pagos en euros entre cualquier cuenta ubicada en la zona de aplicación, se realicen de forma tan sencilla como se viene realizando actualmente un pago nacional, lo que facilita el acceso a los nuevos mercados. Los clientes pueden con una única cuenta, una única tarjeta y unos instrumentos de pago con idénticas características, realizar pagos en cualquiera de los 33 países de la zona SEPA.</p>

                        </br>

                        <h5>¿A qué países afecta el proyecto SEPA? </h5>

                        <p>El <a href="http://www.sepaesp.es/sepa/es/secciones/enlaces/">ámbito geográfico</a> de la SEPA comprende los 28 estados miembros de la Unión Europea (UE), así como Islandia, Lietchestein, Noruega, Suiza y Mónaco.</p>

                        </br>

                        <h5>¿A qué instrumentos de pago afecta el proyecto SEPA?</h5>

                        <p>Los instrumentos de pago SEPA son:</p>

                        </br>

                        <ul>
                            <li><i class="fa fa-caret-right"></i>
                                Transferencias. Contempla: pagos puntuales, masivos, pago de nóminas y pensiones.
                                Adeudos directos (domiciliaciones bancarias).</li>

                            <li><i class="fa fa-caret-right"></i>
                                Comprende: pagos periódicos o puntuales. Se utilizan, bien para pagos nacionales como para pagos transfronterizos en    cualquiera de los países de su ámbito geográfico.</li>

                        </ul>

                        <p>Otro instrumento de pago SEPA son las tarjetas. Las tarjetas válidas en un país de la SEPA ya pueden utilizarse para pagar    y disponer de efectivo en todo el área SEPA con la misma facilidad con la que lo hacen actualmente en sus propios países.</p>

